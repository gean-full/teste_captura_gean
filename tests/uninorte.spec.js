const { test, expect } = require('@playwright/test');
test.use({ launchOptions: { slowMo: 3000 } });

test('Deve fazer login na Uninorte', async ({ page }) => {

    const urlLogin = 'https://ensinc.uninorteac.edu.br/Portal/Aluno/Uninorte/';

    console.log('🎬 Iniciando a navegação (em câmera lenta)...');
    await page.goto(urlLogin);

    const inputUsuario = page.locator('input[name="login"]');
    const inputSenha = page.locator('input[name="password"]');

    console.log('✍️ Inserindo usuario e senha...');
    await inputUsuario.fill('GEANVACARO');
    await inputSenha.fill('gean2000');

    const botaoEntrar = page.getByRole('button', { name: /entrar|login|acessar/i });
    await botaoEntrar.click();
    console.log('Pausa final para o aluno admirar a tela...');
    await page.waitForTimeout(5000);

    await page.screenshot({ path: 'captura_uninorte_aluno.png', fullPage: true });
    console.log('Screenshot da busca salvo!');
});



